    private void btnAgregarCiudadActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarCiudadActionPerformed
        // TODO add your handling code here:

        int i = 1;
        boolean bExiste = false;
        conectar cc = new conectar();
        Connection cn = cc.conexion();
        String sql = "SELECT MAX(idCiudad) as NumMaximo FROM admision_ciudad";
        try {
            java.sql.Statement st = cn.createStatement();
            ResultSet rs = st.executeQuery(sql);

            while (rs.next()) {
                i = rs.getInt("NumMaximo");
            }

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Error al Consultar tipo " + ex);
            ex.printStackTrace();
        }
        for (int j = 0; j <= i; j++) {

            String sql1 = "SELECT Nombre FROM albergue_sanvicente.admision_ciudad ";

            try {
                Statement st = cn.createStatement();
                ResultSet rs = st.executeQuery(sql1);

                while (rs.next()) {
                    if (txtNombre.getText().equals(rs.getString("Nombre"))) {
                        bExiste = true;
                    } else {
                        bExiste = false;
                    }

                }
                if (bExiste == false) {
                    GuardarDatosHoja_Admision();
                    cargar("");
                } else {
                    JOptionPane.showMessageDialog(null, "Ya existe esa ciudad");
                }
                st.close();
                cn.close();
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null, "Error al Consultar tipo " + ex);
            }
        }


    }//GEN-LAST:event_btnAgregarCiudadActionPerformed

