    void cargar(String sValor) {
        String[] titulos = {"Numero Ciudad", "Nombre"};
        String[] registros = new String[2];

        String sql = "SELECT * FROM albergue_sanvicente.admision_ciudad WHERE CONCAT( idCiudad, ' ', Nombre)LIKE '%" + sValor + "%'";
        model = new DefaultTableModel(null, titulos);
        conectar cc = new conectar();
        Connection cn = cc.conexion();

        try {
            Statement st = cn.createStatement();
            ResultSet rs = st.executeQuery(sql);

            while (rs.next()) {
                registros[0] = rs.getString("idCiudad");
                registros[1] = rs.getString("Nombre");

                model.addRow(registros);
            }
            tblRegistros.setModel(model);
        } catch (SQLException ex) {
            ex.printStackTrace();
        }

    }

